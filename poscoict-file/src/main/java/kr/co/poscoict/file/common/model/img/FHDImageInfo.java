package kr.co.poscoict.file.common.model.img;

public class FHDImageInfo extends ImageInfo {
	public FHDImageInfo(ImageShape imageShape) {
		if(imageShape == ImageShape.HORIZONTAL) {
			this.setWidth(IMAGE_SIZE_1980);
			this.setHeight(IMAGE_SIZE_1080);
		} else {
			this.setWidth(IMAGE_SIZE_1080);
			this.setHeight(IMAGE_SIZE_1980);
		}
		this.setImageShape(imageShape);
	}
}
