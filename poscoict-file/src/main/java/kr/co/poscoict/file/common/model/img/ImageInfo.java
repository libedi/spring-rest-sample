package kr.co.poscoict.file.common.model.img;

public abstract class ImageInfo {
	public static final int IMAGE_SIZE_1980 = 1980;
	public static  final int IMAGE_SIZE_1080 = 1080;
	public static  final int IMAGE_SIZE_1280 = 1280;
	public static  final int IMAGE_SIZE_720 = 720;
	
	private int width;
	private int height;
	private ImageShape imageShape;
	
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public ImageShape getImageShape() {
		return imageShape;
	}
	public void setImageShape(ImageShape imageShape) {
		this.imageShape = imageShape;
	}
	
}
